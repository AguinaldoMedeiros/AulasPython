# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool

print('1' + 1) # Vai gerar um erro por não conseguir calcular str com int
print(int('1') + 1) # Ao converter o tipo para int, o cálculo se torna possível