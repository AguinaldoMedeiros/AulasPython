"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strinfs são textos que estão dentro de aspas
"""

# Aspas simples
print('Teste tipo string')

# Aspas duplas
print("Teste tipo string")

# Escape Obs.: Serve para instanciar um caracter sem que seja interpretado pelo sistema como parte da função
print("Teste tipo \"string\"")

# r - expressões regulares Obs.: Mostra as \ no código também
print(r"Teste tipo \"string\"")