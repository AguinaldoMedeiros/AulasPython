# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# postivo ou negativo. int sem sinal é considerado
print(11) # int
print(-11) # int
print(0) # int



# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerdao positivo.
print(1.1) # float
print(-2.2) # float

# A função type mostra o tipo que o Python - type()
# inferiu ao valor.
print(type(11)) # int
print(type(1.1)) # float
print(type("String")) # str
